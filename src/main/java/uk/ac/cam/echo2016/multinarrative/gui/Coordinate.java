package uk.ac.cam.echo2016.multinarrative.gui;

public class Coordinate {
    private double x;
    private double y;

    public Coordinate(double x1, double y1) {
        x = x1;
        y = y1;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}